                        <div class="confetti-burst"></div>
                    </div>
                </div>
                <h2 class="fw-bold mb-1">Quiz Finished</h2>
                <div
                    class="status-badge"
                    :class="score >= 5 ? 'pass' : 'retry'"
                >
                    {{ score >= 5 ? "Excellent Work!" : "Keep Practicing" }}
                </div>
            </div>
            <div class="score-visualization my-5">
                <div class="score-circle-glow">
                    <div class="score-content">
                        <span class="score-main">{{ score }}</span>
                        <span class="score-divider"></span>
                        <span class="score-total">10</span>
                    </div>
                </div>
                <p
                    class="text-muted mt-3 mb-0 small text-uppercase fw-bold ls-1"
                >
                    Final Score
                </p>
            </div>
            <div class="result-actions">
                <button class="btn-retry-premium" @click="OpenQuiz">
                    <i class="bi bi-arrow-clockwise me-2"></i> Try Again
                </button>
            </div>
        </div>
    </div>
</template>
<script>
import QuizData from "../../lesson1/data/numbersAndCountersData";
export default {
    data() {
        return {
            isNumbersAndCountersExample: false,
            isOpenQuiz: false,
            questions: QuizData,
            currentIndex: 0,
            score: 0,
            selectedAnswer: "",
            iscorrectAnswer: false,
            correctAnswer: "",
            isExplanation: false,
            explanation: "",
            showExplanation: false,
            isQuizFinished: false,
            isContinue: false,
        };
    },
    mounted() {
        this.shuffleQuestions();
    },
    computed: {
        currentQuestion() {
            return this.questions[this.currentIndex];
        },
    },
    methods: {
        btnNumbersAndCountersExample() {
            this.isNumbersAndCountersExample =
                !this.isNumbersAndCountersExample;
            if (this.isNumbersAndCountersExample) {
                this.$nextTick(() => {
                    this.$refs.NumberAndCounterExample.scrollIntoView({
                        behavior: "smooth",
                    });
                });
            }
        },
        shuffleQuestions() {
            if (!this.questions) return;
            this.questions = [...this.questions].sort(
                () => Math.random() - 0.5,
            );
        },
        OpenQuiz() {
            this.isOpenQuiz = true;
            this.isQuizFinished = false;
        },
        Getanswer(choice) {
            this.selectedAnswer = choice;
        },
        checkAnswer() {
            // ✅ CORRECT ANSWER
            if (this.selectedAnswer === this.currentQuestion.correctAnswer) {
                this.iscorrectAnswer = true;
                this.isContinue = true;
                this.correctAnswer = "correct";
                this.score++;
                this.$refs.gameSounds.playSound("correct");
            }
            // ❌ WRONG ANSWER
            else {
                this.iscorrectAnswer = false;
                this.correctAnswer = "wrong";
                this.$refs.gameSounds.playSound("wrong");
                this.isContinue = true;
            }
            this.isExplanation = true;
            this.explanation = this.currentQuestion.explanation;
            this.currentQuestion.explanation;
            this.showExplanation = true;
        },
        nextQuestion() {
            this.currentIndex++;
            if (this.currentIndex >= 10) {
                this.currentIndex = 0;
                this.$refs.gameSounds.playSound("finalscores");
                this.isQuizFinished = true;
                this.isOpenQuiz = false;
            }
            this.selectedAnswer = "";
            this.isExplanation = false;
            this.explanation = "";
            this.showExplanation = false;
            this.isContinue = false;
        },
    },
};
</script>
